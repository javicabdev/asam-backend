// MemberIDInfo holds only the ID of a member.
type MemberIDInfo struct {
	MemberID int `db:"id"`
}

// FamilyIDInfo holds only the ID of a family.
type FamilyIDInfo struct {
	FamilyID int `db:"id"`
}

// CashflowGenerator generates test cash movements (caja)
type CashflowGenerator struct {
	db   *sqlx.DB
	rand *rand.Rand
	// Pre-defined operation types and details for efficiency
	operationTypes   []string
	operationDetails map[string][]string
}

